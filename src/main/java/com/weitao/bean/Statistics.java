package com.weitao.bean;

public class Statistics {
    private Integer stId;

    private Double stCount;

    private Integer stNumber;

    private Integer sellerId;

    public Integer getStId() {
        return stId;
    }

    public void setStId(Integer stId) {
        this.stId = stId;
    }

    public Double getStCount() {
        return stCount;
    }

    public void setStCount(Double stCount) {
        this.stCount = stCount;
    }

    public Integer getStNumber() {
        return stNumber;
    }

    public void setStNumber(Integer stNumber) {
        this.stNumber = stNumber;
    }

    public Integer getSellerId() {
        return sellerId;
    }

    public void setSellerId(Integer sellerId) {
        this.sellerId = sellerId;
    }
}